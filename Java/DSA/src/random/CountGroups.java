package random;

import java.util.Scanner;

public class CountGroups {

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int tc = scanner.nextInt();


        scanner.nextLine();

        for (int i = 0; i < tc; i++) {

            String s = scanner.nextLine();

            System.out.println(s);

        }


    }

}